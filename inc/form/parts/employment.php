<h2>Employment History</h2>

<p>The Federal Motor Carrier Safety Regulations (49 CFR 391.21) require that all applicants wishing to drive a commercial vehicle list all employment for the last three (3) years. <strong>In addition, if you have driven a commercial vehicle previously, you must provide employment history for an additional seven (7) years (for a total of ten (10) years). Any gaps in employment in excess of one (1)
month must be explained.</strong></p>
<p>Start with the last or current position, including any military experience, and work backwards (attach separate sheets if necessary). You are required to list the complete mailing address, including street number, city, state, zip; and complete all other information.</p>

